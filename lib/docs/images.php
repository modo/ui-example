<h2>Videos</h2>

<div class="row">
	<div class="span10">
		<h3>Fixed-width video</h3>
<pre class="prettyprint linenums">
<? cleanup_code('<div class="frame-container">
    <div class="frame centered-media">
    	<!-- Youtube or Vimeo embed code -->
    </div>
</div>') ?>
</pre>				

<p><strong>Note:</strong> The wrapper <code>.centered-media</code> is optional. When used, your fixed-width video will be centered using <code>text-align: center;</code>&mdash;for this reason, any text included in the wrapper will also be centered. The <code>.centered-media</code> wrapper can be anywhere in the markup as long as it's a parent of the <code>object</code>, <code>iframe</code>, or <code>embed</code> element</p>

	</div>
	<div class="span10">
		<div class="box">
