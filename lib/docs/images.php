<div class="row">
	<div class="span10">
		<h3>Fluid-width video</h3>
<pre class="prettyprint linenums">
<? cleanup_code('<div class="frame-container">
    <div class="frame fluid-media">
    	<!-- Youtube or Vimeo embed code -->
    </div>
</div>') ?>
</pre>				

<p>Even though fluid videos are done in a similar way as images, since the <a href="http://fitvidsjs.com/" title="fitVid — Fluid video jQuery plugin">method</a> we use to apply 100% width to videos uses absolute positioning, we can't use the <code>.outset</code> &amp; <code>.inset</code> wrappers. However, the <code>.bordered</code> effect works just as it does on images.</p>

